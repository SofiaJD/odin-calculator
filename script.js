function add(num1, num2)
{
    let total = num1 + num2;
    return total;
}

function subtract(num1, num2)
{
    let total = num1 - num2; 
    return total; 
}

function multiply(num1, num2)
{
    let total = num1 * num2;
    return total;
}

function divide(num1, num2)
{
    let total = num1 / num2;
    return total;
}